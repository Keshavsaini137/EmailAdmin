    public void users(){
        
        
        Connection con = null;
        Statement stm = null;
        ResultSet rs = null;
        
        String query = "Select * from user;";
        
        try{
            con = DriverManager.getConnection(jdbc, sqlUsrname, sqlPassword);
            stm = con.prepareStatement(query);
            rs = stm.executeQuery(query);
            
            DefaultTableModel emailTable = (DefaultTableModel) jTable1.getModel();
            
            while(rs.next()){
                String s  = rs.getString(1);
                String s1 = rs.getString(2);
                String s2 = rs.getString(4);
                String s3 = rs.getString(5);
                
                Object[] row = {s,s1,s2,s3};
                emailTable.addRow(row);
                
            } 
            
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
                JOptionPane.showMessageDialog(null, "Cannot connect to the Database");
        }
    
    }
    
    
