    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel userTable = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();

        Connection con = null;
        Statement stm = null;

        String userId = userTable.getValueAt(selectedRow, 0).toString();
        String delete = "Delete from user where UserId = " + userId + ";";

        int a = JOptionPane.showConfirmDialog(rootPane, "Do u want to Delete",
                "Delete", JOptionPane.YES_NO_OPTION);

        if (a == JOptionPane.YES_OPTION) {
            try {
                con = DriverManager.getConnection(jdbc, sqlUsrname, sqlPassword);
                stm = con.prepareStatement(delete);
                stm.executeUpdate(delete);

                JOptionPane.showMessageDialog(null, "User Removed");
                userTable.removeRow(jTable1.getSelectedRow());

            } 
            
            catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
                JOptionPane.showMessageDialog(null, "Cannot connect to the Database");
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

